# TUPLES

# Tuples are immutable (ie. an object with (a) fixed value(s), so they cannot
# be altered)

a = 1234, 4312, "hello"

# On output, tuples are always in parentheses
print(a)

###############################################################################

# Nested tuples

b = a, 1, 2, 3, 4, 5

print(b)

